import java.util.*;
public class switches {
    public static void main(String[] args){
        char ch ='a';
        switch(ch){
            case 'a':System.out.println("Somosa");
            break;
            case 'b':System.out.println("Burger");
            break;
            case 'c':System.out.println("chowmin");
            break;
            default:System.out.println("Wake up");
        }
    }
}
